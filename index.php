    Yagi-Uda Antenna

The Yagi-Uda antenna is one of the most used antenna designs. It typically operates in the HF and UHF bands (about 3 MHz to 3 GHz). This is a highly directive antenna, i.e., it receives strong signals only from the direction in which it is pointed. Yagi-Uda antenna is majorly made up of 4 components, namely, reflectors, directors, driven element/dipole, and a boom. The directional gain of the antenna increases with an increase in the no. of directors. 

The antenna is generally used for TV reception, tracking the International Space Station, and meteorological satellites such as NOAA 15,18, and 19 to get interesting weather images of different parts of the world.
